package qa.learning.tests;// Generated by Selenium IDE
import org.junit.After;
import org.junit.Assert;
import org.junit.Test;
import qa.learning.model.ContactData;

import java.util.Comparator;
import java.util.List;


public class CreateContactTest extends TestBase {

  @Test
  public void testCreateContact() {
    app.getNavigationHelper().gotoHomePage();
    List<ContactData> before = app.getContactHelper().getContactList();
    app.getContactHelper().initContactCreation();
    ContactData contact = new ContactData("Contactname", "Contactsurname", "world", "+79113332255", "test@mail.test");
    app.getContactHelper().fillContactForm(contact);
    app.getContactHelper().submitContactCreation();
    app.getNavigationHelper().gotoHomePage();
    List<ContactData> after = app.getContactHelper().getContactList();
    Assert.assertEquals(before.size() + 1, after.size());
    app.getContactHelper().getContactList();

    before.add(contact);
    Comparator<? super ContactData> byId = Comparator.comparingInt(ContactData::getId);
    before.sort(byId);
    after.sort(byId);
    Assert.assertEquals(before, after);
  }





}
